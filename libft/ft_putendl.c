#include "libft.h"

void	ft_putendl(char const *str)
{
	int i;

	i = 0;
	if (str != NULL)
	{
		while ((char)str[i])
		{
			ft_putchar((char)str[i]);
			i++;
		}
		ft_putchar('\n');
	}
}
